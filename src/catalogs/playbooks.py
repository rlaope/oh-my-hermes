def _all_playbooks() -> tuple[Playbook, ...]:
    return (*_PLAYBOOKS, *_SURFACE_PLAYBOOKS)


def list_playbooks() -> dict[str, object]:
    return {
        "schema_version": PLAYBOOK_CATALOG_SCHEMA_VERSION,
        "playbooks": [playbook.summary_dict() for playbook in _all_playbooks()],
    }


def inspect_playbook(playbook_id: str) -> dict[str, object]:
    playbook = _playbook_by_id(playbook_id)
    return {
        "schema_version": PLAYBOOK_CATALOG_SCHEMA_VERSION,
        "playbook": playbook.to_dict(),
    }


def recommend_playbooks(query: str, *, limit: int = 3) -> dict[str, object]:
    if limit < 1:
        raise ValueError("playbook recommend --limit must be at least 1")
    task = query.strip()
    if not task:
        raise ValueError("playbook recommend requires a task description")
    routing_text = prepare_routing_text(task)
    query_tokens = _tokens(routing_text.scoring_text)
    query_terms = _terms(routing_text.scoring_text)
    scored = [
        _score_playbook(profile, query_tokens, query_terms, routing_text.locale_matches)
        for profile in _playbook_scoring_profiles()
    ]
    scored.sort(key=lambda item: (-int(item["score"]), str(item["id"])))
    matches = [item for item in scored if int(item["score"]) > 0] or [_fallback_playbook(task)]
    return {
        "schema_version": PLAYBOOK_RECOMMENDATION_SCHEMA_VERSION,
        "query": task,
        "recommendations": matches[:limit],
    }


def _playbook_by_id(playbook_id: str) -> Playbook:
    for playbook in _all_playbooks():
        if playbook.id == playbook_id:
            return playbook
    raise KeyError(playbook_id)


def _score_playbook(
    profile: _PlaybookScoringProfile,
    query_tokens: set[str],
    query_terms: set[str],
    locale_matches: tuple[str, ...] = (),
) -> dict[str, object]:
    playbook = profile.playbook
    score = 0
    matched: set[str] = set()

    for keyword in profile.keyword_terms:
        if _matches_term_tokens(keyword.tokens, query_terms):
            score += 5
            matched.add(f"keyword:{keyword.label}")

    for token in sorted(query_tokens & profile.intent_tokens):
        score += 2
        matched.add(f"intent:{token}")

    for intent_tag in profile.intent_tag_terms:
        if _matches_term_tokens(intent_tag.tokens, query_terms):
            score += 3
            matched.add(f"intent-tag:{intent_tag.label}")

    if "coding" in query_tokens or "code" in query_tokens or "implement" in query_tokens:
        if playbook.delegated_to_executor:
            score += 2
            matched.add("boundary:executor")
    if "research" in query_tokens or "source" in query_tokens:
        if not playbook.delegated_to_executor:
            score += 2
            matched.add("boundary:hermes")

    if score > 0:
        matched.update(f"locale:{match}" for match in locale_matches)

    return _recommendation_payload(playbook, score=score, matched=tuple(sorted(matched)))


def _fallback_playbook(query: str) -> dict[str, object]:
    playbook = _playbook_by_id("deep-interview-to-plan")
    payload = _recommendation_payload(playbook, score=0, matched=())
    payload["why"] = "No strong playbook match; start by clarifying the request before planning or handoff."
    payload["suggested_prompt"] = f"Clarify and plan this request before delegation: {query}"
    return payload


def _recommendation_payload(playbook: Playbook, *, score: int, matched: tuple[str, ...]) -> dict[str, object]:
    first_stage = playbook.stages[0]
    return {
        "id": playbook.id,
        "title": playbook.title,
        "summary": playbook.summary,
        "score": score,
        "confidence": _confidence(score),
        "matched": list(matched),
        "why": _why(matched),
        "pipeline": list(playbook.pipeline),
        "next_action": first_stage.id,
        "wrapper_actions": list(first_stage.wrapper_actions),
        "evidence_boundary": first_stage.evidence_boundary,
        "retained_by_hermes": list(playbook.retained_by_hermes),
        "delegated_to_executor": list(playbook.delegated_to_executor),
        "not_evidence_until_observed": list(playbook.not_evidence_until_observed),
        "suggested_prompt": f"Use the {playbook.id} playbook for this request.",
    }


def _tokens(value: str) -> set[str]:
    return set(_tokens_cached(value))


def _terms(value: str) -> set[str]:
    return set(_terms_cached(value))


def _matches_term(term: str, query_terms: set[str]) -> bool:
    return _matches_term_tokens(_normalized_term_tokens(term), query_terms)


def _matches_term_tokens(term_tokens: tuple[str, ...], query_terms: set[str]) -> bool:
    return bool(term_tokens) and query_terms.issuperset(term_tokens)


@lru_cache(maxsize=1)
def _playbook_scoring_profiles() -> tuple[_PlaybookScoringProfile, ...]:
    return tuple(_build_scoring_profile(playbook) for playbook in _all_playbooks())


def _build_scoring_profile(playbook: Playbook) -> _PlaybookScoringProfile:
    return _PlaybookScoringProfile(
        playbook=playbook,
        keyword_terms=tuple(_scoring_term(keyword.lower()) for keyword in playbook.keywords),
        intent_tokens=_tokens_cached(" ".join((playbook.id, *playbook.intent_tags))),
        intent_tag_terms=tuple(_scoring_term(intent_tag.lower()) for intent_tag in playbook.intent_tags),
    )


def _scoring_term(label: str) -> _ScoringTerm:
    return _ScoringTerm(label=label, tokens=_normalized_term_tokens(label))


@lru_cache(maxsize=512)
def _tokens_cached(value: str) -> frozenset[str]:
    return frozenset(routing_tokens(value, stopwords=_STOPWORDS))


@lru_cache(maxsize=1024)
def _terms_cached(value: str) -> frozenset[str]:
    return frozenset(routing_terms(value))


@lru_cache(maxsize=1024)
def _normalized_term_tokens(term: str) -> tuple[str, ...]:
    return tuple(_terms_cached(normalized_phrase(term)))


def _confidence(score: int) -> str:
    if score >= 10:
        return "high"
    if score >= 4:
        return "medium"
    return "low"


def _why(matched: tuple[str, ...]) -> str:
    if not matched:
        return "No strong playbook match."
    sources = sorted({item.split(":", 1)[0] for item in matched})
    return f"Matched {'/'.join(sources)} playbook signals for this task."
