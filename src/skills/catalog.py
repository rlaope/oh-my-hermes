OMH_DESCRIPTION_PREFIX = "[omh] "


def omh_description(description: str) -> str:
    text = description.strip()
    if text.lower().startswith(OMH_DESCRIPTION_PREFIX):
        return text
    return f"{OMH_DESCRIPTION_PREFIX}{text}"


